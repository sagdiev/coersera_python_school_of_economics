# Входные данные:
# 179
#
# Вывод программы:
# The next number for the number 179 is 180.
# The previous number for the number 179 is 178.

a = int(input())
print('The next number for the number', a, 'is', a + 1, sep=' ', end='.\n')
print('The previous number for the number', a, 'is', a - 1, sep=' ', end='.')
